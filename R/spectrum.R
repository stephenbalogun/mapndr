#' The Harvested Spectrum Data put together by the Authors
#'
#' @param major optional. The major version number of the spectrum. Currently only contains version 6
#' @param minor optional. The minor version number of the spectrum. Currently only contains version 19
#' @param year optional. The spectrum year to be returned
#' @param state optional. The state of interest.
#'
#' @return rectangular data of interest
#' @export
#'
#' @examples
#' ## get Abia state spectrum estimate for the different age-bands and gender in 2022
#' abia_2022 <- spectrum(year = 2022, state = "Abia")
#'
#' ## get spectrum estimate for Adamawa and Rivers in 2023
#' ada_riv_2023 <- spectrum(year = 2023, state = c("Adamawa", "Rivers"))
spectrum <- function(major = 6, minor = 19, year = 2022, state = NULL) {
  if (!is.numeric(year) || !is.numeric(major) || !is.numeric(minor)) {
    rlang::abort("The values to `year`, `major` and `minor` must all be integers!")
  }

  if (nchar(major) != 1 || nchar(minor) > 2) {
    rlang::abort("`major` and `minor` values should be supplied separately and as whole numbers")
  }

  if (!is.null(state) && all(!state %in% c(naijR::states(), "FCT"))) {
    rlang::abort("state must be any or a combination of the recognized Nigeria states based on NDR format")
  }

  df <- if (major == 6 && minor == 19) {
    spectrum_619() |>
      dplyr::filter(
        .data$year %in% {{ year }}
      )
  } else {
    rlang::inform("Only `spectrum 6.19` is currently available at the moment. Please use that or check back later")
  }

  if (!is.null(state)) {
    df |>
      dplyr::filter(.data$state %in% {{ state }})
  } else {
    df
  }
}


